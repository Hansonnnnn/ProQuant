package PO.kLinePO;

public class Kdj {
	private Double k;
	private Double d;
	private Double j;
	public Double getK() {
		return k;
	}
	public void setK(Double k) {
		this.k = k;
	}
	public Double getD() {
		return d;
	}
	public void setD(Double d) {
		this.d = d;
	}
	public Double getJ() {
		return j;
	}
	public void setJ(Double j) {
		this.j = j;
	}
	@Override
	public String toString() {
		return "kdj [k=" + k + ", d=" + d + ", j=" + j + "]";
	}
	
	
}
